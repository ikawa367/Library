public class D2 extends Shape {
    private double surface; // masahat
    private double perimeter; // mohit

    public D2() {
        surface = 0;
        perimeter = 0;
    }

    public void setSurface(double input) {
        surface = input;
    }

    public double getSurface() {
        return surface;
    }

    public void setPerimeter(double input) {
        perimeter = input;
    }

    public double getPerimeter() {
        return perimeter;
    }
}
